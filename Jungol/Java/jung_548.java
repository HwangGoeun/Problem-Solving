package Java;

public class jung_548 {
    public static void main(String[] args) {

        for (int i = 2; i < 5; i++) {
            for (int j = 1; j < 6; j++) {
                System.out.print(i + " * " + j + " = ");
                System.out.printf("%2d   ", i * j);
            }
            System.out.println("");
        }
    }
    
}
